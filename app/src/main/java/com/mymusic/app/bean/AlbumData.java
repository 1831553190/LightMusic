package com.mymusic.app.bean;

public class AlbumData {
	String albumTitle;
	long albumId;
	String albumArtCover;
	String albumArtist;
	String albumKey;
	String albumFirstYear;
	String albumLastYear;
	int count;
	
	
	public int getCount() {
		return count;
	}
	
	public void setCount(int count) {
		this.count = count;
	}
	
	
	public String getAlbumArtCover() {
		return albumArtCover;
	}
	
	public void setAlbumArtCover(String albumArtCover) {
		this.albumArtCover = albumArtCover;
	}
	
	public String getAlbumArtist() {
		return albumArtist;
	}
	
	public void setAlbumArtist(String albumArtist) {
		this.albumArtist = albumArtist;
	}
	
	public String getAlbumTitle() {
		return albumTitle;
	}
	
	public void setAlbumTitle(String albumTitle) {
		this.albumTitle = albumTitle;
	}
	
	public long getAlbumId() {
		return albumId;
	}
	
	public void setAlbumId(long albumId) {
		this.albumId = albumId;
	}
	
	
	public String getAlbumKey() {
		return albumKey;
	}
	
	public void setAlbumKey(String albumKey) {
		this.albumKey = albumKey;
	}
	
	public String getAlbumFirstYear() {
		return albumFirstYear;
	}
	
	public void setAlbumFirstYear(String albumFirstYear) {
		this.albumFirstYear = albumFirstYear;
	}
	
	public String getAlbumLastYear() {
		return albumLastYear;
	}
	
	public void setAlbumLastYear(String albumLastYear) {
		this.albumLastYear = albumLastYear;
	}
	
}
